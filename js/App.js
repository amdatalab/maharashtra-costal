window.onload = function () {
    const selectDistrict = document.getElementById('district'),
        selectTaluka = document.getElementById('taluka'),
        selectVillage = document.getElementById('village'),
        selects = document.querySelectorAll('select')

    // selectDistrict.disabled = true
    selectTaluka.disabled = true
    selectVillage.disabled = true

    selects.forEach(select => {
        if (select.disabled == true) {
            select.style.cursor = "auto"
        }
    })

    for (let district in districtTalukaVillageinfo) {
        console.log(district);
        selectDistrict.options[selectDistrict.options.length] = new Option(district, district);
    }
    selectDistrict.onchange = (e) => {
        selectDistrict.disabled = false
        selectTaluka.disabled = true
        selectVillage.disabled = true
    }

    //district change
    selectDistrict.onchange = (e) => {
        selectTaluka.disabled = false
        selectVillage.disabled = true

        selectTaluka.length = 1
        selectVillage.length = 1

        for (let taluka in districtTalukaVillageinfo[e.target.value]) {
            console.log(taluka);
            selectTaluka.options[selectTaluka.options.length] = new Option(taluka, taluka);
        }
    }

    //taluka change
    selectTaluka.onchange = (e) => {

        selectVillage.disabled = false

        selectVillage.length = 1

        for (let village in districtTalukaVillageinfo[selectDistrict.value][e.target.value]) {
            console.log(village);
            selectVillage.options[selectVillage.options.length] = new Option(village, village);
        }
    }
}